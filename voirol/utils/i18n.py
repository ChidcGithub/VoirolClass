def set_language(lang: str):
    global _current_lang
    normalized = lang.split("-")[0] if lang else "en"
    _current_lang = normalized if normalized in _STRINGS else "en"


def get_language() -> str:
    return _current_lang


def t(key: str, **kwargs) -> str:
    s = _STRINGS.get(_current_lang, _STRINGS["en"]).get(key, key)
    if kwargs:
        s = s.format(**kwargs)
    return s


def state_name(state_value: str) -> str:
    return _PIPELINE_STATES.get(_current_lang, _PIPELINE_STATES["en"]).get(state_value, state_value)
